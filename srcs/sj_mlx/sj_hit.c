#include "../../include/cub3d.h"

void	sj_hit(t_cub *cub)
{
	if (C->C->side == 0)
	{
		if (C->C->mapx < C->C->pos_x)
			C->C->side = 1;
	}
	else
	{
		if (C->C->mapy < C->C->pos_y)
			C->C->side = 3;
	}
}
